#Backwords traversal with while loop
fruit = 'fruit'
frukt = len(fruit) -1
while frukt >= 0:
    print(fruit[frukt])
    frukt = frukt - 1 

# Traversal with for loop
fruitti = 'fruit'

for i in fruitti:
    print(i)

#Slicing. Slicing a string in to pieces. A segment of a string is called a slice
slicing = 'a long string that can be sliced'
print(slicing[1:10])
